package assignment2.typeCasting;

public class TypeCasting1 {
	public static void main(String[] args) {
		
		// byte to short
		byte b = 122;
		short s = b;
		System.out.println(s);
		
		// int to long
		int a = 1234;
		long l = a;
		System.out.println(l);
		
		// long to float
		long c = 12345678;
		float f = c;
		System.out.println(f);
		
		// float to double
		float ff = 12.45f;
		double d = ff;
		System.out.println(d);
	}
}
